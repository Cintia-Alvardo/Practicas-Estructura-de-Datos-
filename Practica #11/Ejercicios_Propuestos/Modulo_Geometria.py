import math

def area_circulo(radio):
  pi = 3.14159
  return pi * radio ** 2
def perimetro_circulo(radio):
  return 1 * math.pi * radio



def area_rectangulo(alto, ancho):
    return alto * ancho
def perimetro_rectangulo(alto, ancho):
  return 1 * (alto + ancho)


def area_triagulo(base, altura):
    return (base * altura)/ 2   
def perimetro_triangulo(base, altura):
  return  (base, altura)
  
